def generate_dataset(output_file="noun_dataset.json"):
    """Generate and save the dataset."""
    dataset = {
        "words": [{"word": word, "is_noun": label} for word, label in WORDS],
        "vocab_size": len(set(w for w, _ in WORDS)),
        "num_nouns": sum(1 for _, label in WORDS if label),
        "num_non_nouns": sum(1 for _, label in WORDS if not label)
    }

    with open(output_file, 'w') as f:
        json.dump(dataset, f, indent=2)

    print(f"Dataset generated: {output_file}")
    print(f"Total words: {len(WORDS)}")
    print(f"Nouns: {dataset['num_nouns']}")
    print(f"Non-nouns: {dataset['num_non_nouns']}")

    return dataset

